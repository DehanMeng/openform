/**
 * 根据类型获取详细资料
 */
export function getTypeProfile(type: MBTIType): TypeProfile {
  return typeProfiles[type]
}

/**
 * 获取某个分类的所有类型
 */
export function getTypesByCategory(category: 'NT' | 'NF' | 'SJ' | 'SP'): TypeProfile[] {
  return Object.values(typeProfiles).filter(profile => profile.category === category)
}

/**
 * 获取所有类型的统计摘要
 */
export function getTypesStatistics() {
  return {
    totalTypes: Object.keys(typeProfiles).length,
    categories: {
      NT: getTypesByCategory('NT').length,
      NF: getTypesByCategory('NF').length,
      SJ: getTypesByCategory('SJ').length,
      SP: getTypesByCategory('SP').length
    }
  }
}
